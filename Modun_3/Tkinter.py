#Thêm thư viện tkinter
from tkinter import *
#Tạo một cửa sổ mới
window = Tk()
#Thêm tiêu đề cho cửa sổ
window.title('Welcome to PythonPCAP app')
#Đặt kích thước của cửa sổ
window.geometry('350x200')
# Lặp vô tận để hiển thị cửa sổ - Hàm này gọi vòng lặp vô tận của 
# cửa sổ, vì vậy cửa sổ sẽ chờ bất kỳ tương tác người dùng nào cho 
# đến khi đóng nó.
window.mainloop()
